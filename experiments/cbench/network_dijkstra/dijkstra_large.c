int NUM_NODES = 0;
struct _NODE {
    int iDist;
    int iPrev;
};
typedef struct _NODE NODE;
struct _QITEM {
    int iNode;
    int iDist;
    int iPrev;
    struct _QITEM *qNext;
};
typedef struct _QITEM QITEM;
QITEM *qHead = ((void *) 0);
int *AdjMatrix;
int g_qCount = 0;
NODE *rgnNodes;
int ch;
int iPrev, iNode;
int i, iCost, iDist;
void print_path(NODE * rgnNodes, int chNode)
{
    if (rgnNodes[chNode].iPrev != 9999) {
	print_path(rgnNodes, rgnNodes[chNode].iPrev);
    }
    printf(" %d", chNode);
    fflush(stdout);
}

void enqueue(int iNode, int iDist, int iPrev)
{
    QITEM *qNew = (QITEM *) malloc(sizeof(QITEM));
    QITEM *qLast = qHead;
    if (!qNew) {
	fprintf(stderr, "Out of memory.\n");
	exit(1);
    }
    qNew->iNode = iNode;
    qNew->iDist = iDist;
    qNew->iPrev = iPrev;
    qNew->qNext = ((void *) 0);
    if (!qLast) {
	qHead = qNew;
    } else {
	while (qLast->qNext)
	    qLast = qLast->qNext;
	qLast->qNext = qNew;
    }
    g_qCount++;
}

void dequeue(int *piNode, int *piDist, int *piPrev)
{
    QITEM *qKill = qHead;
    if (qHead) {
	*piNode = qHead->iNode;
	*piDist = qHead->iDist;
	*piPrev = qHead->iPrev;
	qHead = qHead->qNext;
	free(qKill);
	g_qCount--;
    }
}

int qcount(void)
{
    return (g_qCount);
}

void dijkstra(int chStart, int chEnd)
{
    for (ch = 0; ch < NUM_NODES; ch++) {
	rgnNodes[ch].iDist = 9999;
	rgnNodes[ch].iPrev = 9999;
    }
    if (chStart == chEnd) {
	printf("Shortest path is 0 in cost. Just stay where you are.\n");
    } else {
	rgnNodes[chStart].iDist = 0;
	rgnNodes[chStart].iPrev = 9999;
	enqueue(chStart, 0, 9999);
	while (qcount() > 0) {
	    dequeue(&iNode, &iDist, &iPrev);
	    for (i = 0; i < NUM_NODES; i++) {
		if ((iCost = AdjMatrix[iNode * NUM_NODES + i]) != 9999) {
		    if ((9999 == rgnNodes[i].iDist) || (rgnNodes[i].iDist > (iCost + iDist))) {
			rgnNodes[i].iDist = iDist + iCost;
			rgnNodes[i].iPrev = iNode;
			enqueue(i, iDist + iCost, iNode);
		    }
		}
	    }
	}
    }
}
