int gs_setbbox(gs_state * pgs, floatp llx, floatp lly, floatp urx, floatp ury)
{
    gs_rect ubox, dbox;
    gs_fixed_rect obox, bbox;
    gx_path *ppath = pgs->path;
    int code;
    if (llx > urx || lly > ury)
	return ((-15));
    ubox.p.x = llx;
    ubox.p.y = lly;
    ubox.q.x = urx;
    ubox.q.y = ury;
    if ((code = gs_bbox_transform(&ubox, &(*(const gs_matrix *) &(pgs)->ctm), &dbox)) < 0)
	return code;
    if (dbox.p.x < (((-1L << ((1 << 2) * 8 - 1)) + (1L * 3)) * (1.0 / (1 << 12))) || dbox.p.y < (((-1L << ((1 << 2) * 8 - 1)) + (1L * 3)) * (1.0 / (1 << 12))) || dbox.q.x >= (((~(-1L << ((1 << 2) * 8 - 1))) - (1L * 3) + 1L) * (1.0 / (1 << 12))) || dbox.q.y >= (((~(-1L << ((1 << 2) * 8 - 1))) - (1L * 3) + 1L) * (1.0 / (1 << 12))))
	return ((-13));
    bbox.p.x = (fixed) floor(dbox.p.x * (1 << 12)) - (1L * 3);
    bbox.p.y = (fixed) floor(dbox.p.y * (1 << 12)) - (1L * 3);
    bbox.q.x = (fixed) ceil(dbox.q.x * (1 << 12)) + (1L * 3);
    bbox.q.y = (fixed) ceil(dbox.q.y * (1 << 12)) + (1L * 3);
    if (gx_path_bbox(ppath, &obox) >= 0) {
	ppath->bbox.p.x = (((obox.p.x) < (bbox.p.x)) ? (obox.p.x) : (bbox.p.x));
	ppath->bbox.p.y = (((obox.p.y) < (bbox.p.y)) ? (obox.p.y) : (bbox.p.y));
	ppath->bbox.q.x = (((obox.q.x) > (bbox.q.x)) ? (obox.q.x) : (bbox.q.x));
	ppath->bbox.q.y = (((obox.q.y) > (bbox.q.y)) ? (obox.q.y) : (bbox.q.y));
    } else {
	ppath->bbox = bbox;
    }
    ppath->bbox_set = 1;
    return 0;
}
