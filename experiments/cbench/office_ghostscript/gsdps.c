int gs_rectappend(gs_state * pgs, const gs_rect * pr, uint count)
{
    for (; count != 0; count--, pr++) {
	floatp px = pr->p.x, py = pr->p.y, qx = pr->q.x, qy = pr->q.y;
	int code;
	if ((qx >= px) != (qy >= py))
	    qx = px, px = pr->q.x;
	if ((code = gs_moveto(pgs, px, py)) < 0 || (code = gs_lineto(pgs, qx, py)) < 0 || (code = gs_lineto(pgs, qx, qy)) < 0 || (code = gs_lineto(pgs, px, qy)) < 0 || (code = gs_closepath(pgs)) < 0)
	    return code;
    }
    return 0;
}

int gs_rectclip(gs_state * pgs, const gs_rect * pr, uint count)
{
    int code;
    gx_path old_path;
    old_path = *pgs->path;
    gx_path_reset(pgs->path);
    if ((code = gs_rectappend(pgs, pr, count)) < 0 || (code = gs_clip(pgs)) < 0) {
	gx_path_release(pgs->path);
	*pgs->path = old_path;
	return code;
    }
    gs_newpath(pgs);
    gx_path_release(&old_path);
    return 0;
}
