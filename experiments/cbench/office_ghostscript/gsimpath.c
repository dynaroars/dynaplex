typedef struct {
    gs_state *pgs;
    const byte *data;
    int width, height, raster;
    int dx, dy;
    int count;
} status;
static int get_pixel(const status *, int, int);
static int trace_from(status *, int, int, int);
static int add_dxdy(status *, int, int, int);

//complexity is O(n^2) inferred by loopus
int gs_imagepath(gs_state * pgs, int width, int height, const byte * data)
{
    status stat;
    status *out = &stat;
    int code, x, y;
    stat.pgs = pgs;
    stat.data = data;
    stat.width = width;
    stat.height = height;
    stat.raster = (width + 7) / 8;
    for (y = height - 1; y >= 0; y--)
	for (x = width - 1; x >= 0; x--) {
	    if (get_pixel(out, x, y) && !get_pixel(out, x, y - 1) && (!get_pixel(out, x + 1, y) || get_pixel(out, x + 1, y - 1)) && !trace_from(out, x, y, 1)) {
		stat.count = 0;
		stat.dx = stat.dy = 0;
		if ((code = trace_from(out, x, y, 0)) < 0)
		    return code;
		if ((code = add_dxdy(out, 0, 0, 1)) < 0)
		    return code;
		if ((code = gs_closepath(pgs)) < 0)
		    return code;
	    }
	}
    return 0;
}

static int get_pixel(register const status * out, int x, int y)
{
    if (x < 0 || x >= out->width || y < 0 || y >= out->height)
	return 0;
    return (out->data[y * out->raster + (x >> 3)] >> (~x & 7)) & 1;
}

static int trace_from(register status * out, int x0, int y0, int detect)
{
    int x = x0, y = y0;
    int dx = -1, dy = 0;
    int part = 0;
    int code;
    if (!detect) {
	part = (get_pixel(out, x + 1, y - 1) ? 4 - 1 : 1);
	code = gs_moveto(out->pgs, x + 1 - part / (float) 4, (float) y);
	if (code < 0)
	    return code;
    }
    while (1) {
	int tx = dx - dy, ty = dy + dx;
	if (get_pixel(out, x + tx, y + ty)) {
	    if (!detect) {
		if (out->dx == ty && out->dy == -tx) {
		    out->count -= (4 / 2 - 1);
		    if ((code = add_dxdy(out, tx, ty, 4 / 2)) < 0)
			return code;
		} else {
		    if ((code = add_dxdy(out, dx, dy, 1 - part)) < 0)
			return code;
		    if ((code = add_dxdy(out, tx, ty, 4 - 1)) < 0)
			return code;
		}
		part = 4 - 1;
	    }
	    x += tx, y += ty;
	    dx = -dy, dy += tx;
	} else if (!get_pixel(out, x + dx, y + dy)) {
	    if (!detect) {
		if ((code = add_dxdy(out, dx, dy, 4 - 1 - part)) < 0)
		    return code;
		if ((code = add_dxdy(out, ty, -tx, 1)) < 0)
		    return code;
		part = 1;
	    }
	    dx = dy, dy -= ty;
	} else {
	    if (!detect) {
		if ((code = add_dxdy(out, dx, dy, 4)) < 0)
		    return code;
	    }
	    x += dx, y += dy;
	}
	if (dx == -1 && dy == 0 && !(tx == -1 && ty == -1)) {
	    if (x == x0 && y == y0)
		return 0;
	    if (detect && (y > y0 || (y == y0 && x > x0)))
		return 1;
	}
    }
}

static int add_dxdy(register status * out, int dx, int dy, int count)
{
    if (count != 0) {
	if (dx == out->dx && dy == out->dy)
	    out->count += count;
	else {
	    if (out->count != 0) {
		int code = gs_rlineto(out->pgs, out->dx * out->count / (float) 4, out->dy * out->count / (float) 4);
		if (code < 0)
		    return code;
	    }
	    out->dx = dx, out->dy = dy;
	    out->count = count;
	}
    }
    return 0;
}
