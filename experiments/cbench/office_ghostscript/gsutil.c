void memflip8x8(const byte * inp, int line_size, byte * outp, int dist)
{
    register uint ae, bf, cg, dh; {
	const byte *ptr4 = inp + (line_size << 2);
	ae = ((uint) * inp << 8) + *ptr4;
	inp += line_size, ptr4 += line_size;
	bf = ((uint) * inp << 8) + *ptr4;
	inp += line_size, ptr4 += line_size;
	cg = ((uint) * inp << 8) + *ptr4;
	inp += line_size, ptr4 += line_size;
	dh = ((uint) * inp << 8) + *ptr4;
    } if (ae == bf && ae == cg && ae == dh && (ae >> 8) == (ae & 0xff)) {
	if (ae == 0)
	    goto store;
	*outp = -((ae >> 7) & 1);
	outp += dist;
	*outp = -((ae >> 6) & 1);
	outp += dist;
	*outp = -((ae >> 5) & 1);
	outp += dist;
	*outp = -((ae >> 4) & 1);
	outp += dist;
	*outp = -((ae >> 3) & 1);
	outp += dist;
	*outp = -((ae >> 2) & 1);
	outp += dist;
	*outp = -((ae >> 1) & 1);
	outp += dist;
	*outp = -(ae & 1);
	return;
    }
    {
	register uint temp;
	ae ^= (temp = ((ae >> 4) ^ ae) & 0x00f0);
	ae ^= temp << 4;
	bf ^= (temp = ((bf >> 4) ^ bf) & 0x00f0);
	bf ^= temp << 4;
	cg ^= (temp = ((cg >> 4) ^ cg) & 0x00f0);
	cg ^= temp << 4;
	dh ^= (temp = ((dh >> 4) ^ dh) & 0x00f0);
	dh ^= temp << 4;
	ae ^= (temp = ((cg >> 2) ^ ae) & 0x3333);
	cg ^= temp << 2;
	bf ^= (temp = ((dh >> 2) ^ bf) & 0x3333);
	dh ^= temp << 2;
	ae ^= (temp = ((bf >> 1) ^ ae) & 0x5555);
	bf ^= temp << 1;
	cg ^= (temp = ((dh >> 1) ^ cg) & 0x5555);
	dh ^= temp << 1;
  } store:*outp = ae >> 8;
    outp += dist;
    *outp = bf >> 8;
    outp += dist;
    *outp = cg >> 8;
    outp += dist;
    *outp = dh >> 8;
    outp += dist;
    *outp = (byte) ae;
    outp += dist;
    *outp = (byte) bf;
    outp += dist;
    *outp = (byte) cg;
    outp += dist;
    *outp = (byte) dh;
}
//complexity is O(n) inferred by loopus
 int bytes_compare(const byte * s1, uint len1, const byte * s2, uint len2)
{
    register uint len = len1;
    if (len2 < len)
	len = len2; {
	register const byte *p1 = s1;
	register const byte *p2 = s2;
	while (len--)
	    if (*p1++ != *p2++)
		return (p1[-1] < p2[-1] ? -1 : 1);
	}
	return (len1 == len2 ? 0 : len1 < len2 ? -1 : 1);
}
static const string_match_params smp_default = { '*', '?', '\\', ((bool) 0) };

bool string_match(const byte * str, uint len, const byte * pstr, uint plen, register const string_match_params * psmp)
{
    const byte *pback = 0;
    const byte *spback = 0;
    const byte *p = pstr, *pend = pstr + plen;
    const byte *sp = str, *spend = str + len;
    if (psmp == 0)
	psmp = &smp_default;
  again:while (p < pend) {
	register byte ch = *p;
	if (ch == psmp->any_substring) {
	    pback = ++p, spback = sp;
	    continue;
	} else if (ch == psmp->any_char) {
	    if (sp == spend)
		return ((bool) 0);
	    p++, sp++;
	    continue;
	} else if (ch == psmp->quote_next) {
	    if (++p == pend)
		return ((bool) 1);
	    ch = *p;
	}
	if (sp == spend)
	    return ((bool) 0);
	if (*sp == ch || (psmp->ignore_case && (*sp ^ ch) == 0x20 && (ch &= ~0x20) >= 0x41 && ch <= 0x5a))
	    p++, sp++;
	else if (pback == 0)
	    return ((bool) 0);
	else {
	    sp = ++spback;
	    p = pback;
	}
    }
    if (sp < spend) {
	if (pback == 0)
	    return ((bool) 0);
	p = pback;
	pback = 0;
	sp = spend - (pend - p);
	goto again;
    }
    return ((bool) 1);
}

bool uid_equal(register const gs_uid * puid1, register const gs_uid * puid2)
{
    if (puid1->id != puid2->id)
	return ((bool) 0);
    if (puid1->id >= 0)
	return ((bool) 1);
    return !memcmp((const char *) puid1->xvalues, (const char *) puid2->xvalues, (uint) - (puid1->id) * sizeof(long));
}
