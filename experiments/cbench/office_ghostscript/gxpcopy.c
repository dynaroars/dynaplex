int gx_path_copy_reducing(const gx_path * ppath_old, gx_path * ppath, fixed fixed_flatness, gx_path_copy_options options)
{
    gx_path old;
    const segment *pseg;
    int code;
    old = *ppath_old;
    if (options & pco_init)
	gx_path_init(ppath, ppath_old->memory);
    pseg = (const segment *) (old.first_subpath);
    while (pseg) {
	switch (pseg->type) {
	case s_start:
	    code = gx_path_add_point(ppath, pseg->pt.x, pseg->pt.y);
	    break;
	case s_curve:{
		const curve_segment *pc = (const curve_segment *) pseg;
		if (fixed_flatness == (~(-1L << ((1 << 2) * 8 - 1)))) {
		    if (options & pco_monotonize)
			code = monotonize_internal(ppath, pc);
		    else
			code = gx_path_add_curve_notes(ppath, pc->p1.x, pc->p1.y, pc->p2.x, pc->p2.y, pc->pt.x, pc->pt.y, pseg->notes);
		} else {
		    fixed x0 = ppath->position.x;
		    fixed y0 = ppath->position.y;
		    int k = gx_curve_log2_samples(x0, y0, pc, fixed_flatness);
		    segment_notes notes = pseg->notes;
		    segment *start;
		    curve_segment cseg;
		    if (options & pco_accurate) {
			code = gx_path_add_line_notes(ppath, x0, y0, notes);
			if (code < 0)
			    break;
			start = ppath->current_subpath->last;
			notes |= sn_not_first;
		    }
		    cseg = *pc;
		    code = gx_flatten_sample(ppath, k, &cseg, notes);
		    if (options & pco_accurate) {
			segment *end = ppath->current_subpath->last;
			if (code < 0 || (code = gx_path_add_line_notes(ppath, ppath->position.x, ppath->position.y, pseg->notes | sn_not_first)) < 0)
			    break;
			adjust_point_to_tangent(start, start->next, &pc->p1);
			adjust_point_to_tangent(end, end->prev, &pc->p2);
		    }
		}
		break;
	    }
	case s_line:
	    code = gx_path_add_line_notes(ppath, pseg->pt.x, pseg->pt.y, pseg->notes);
	    break;
	case s_line_close:
	    code = gx_path_close_subpath_notes(ppath, sn_none);
	    break;
	default:
	    code = ((-29));
	}
	if (code < 0) {
	    gx_path_release(ppath);
	    if (ppath == ppath_old)
		*ppath = old;
	    return code;
	}
	pseg = pseg->next;
    }
    if ((((&old)->state_flags & ~psf_outside_range) == psf_last_moveto))
	gx_path_add_point(ppath, old.position.x, old.position.y);
    return 0;
}

static void adjust_point_to_tangent(segment * pseg, const segment * next, const gs_fixed_point * p1)
{
    fixed x0 = pseg->pt.x, y0 = pseg->pt.y;
    double C = p1->x - x0, D = p1->y - y0;
    if (!(C == 0 && D == 0)) {
	double T = (C * (next->pt.x - x0) + D * (next->pt.y - y0)) * 0.3 / (C * C + D * D);
	do {
	} while (0);
	if (T >= 0) {
	    pseg->pt.x = (fixed) (C * T) + x0;
	    pseg->pt.y = (fixed) (D * T) + y0;
	}
    }
}

void gx_curve_cursor_init(curve_cursor * prc, fixed x0, fixed y0, const curve_segment * pc, int k)
{
    fixed v01, v12;
    int k2 = k + k, k3 = k2 + k;
    prc->k = k;
    prc->p0.x = x0, prc->p0.y = y0;
    prc->pc = pc; {
	fixed w0, w1, w2, w3;
	if (y0 < pc->pt.y)
	    w0 = x0, w1 = pc->p1.x, w2 = pc->p2.x, w3 = pc->pt.x;
	else
	    w0 = pc->pt.x, w1 = pc->p2.x, w2 = pc->p1.x, w3 = x0;
	(v01 = (w1) - (w0), prc->c = (v01 << 1) + v01, v12 = (w2) - (w1), prc->b = (v12 << 1) + v12 - prc->c, prc->a = (w3) - prc->b - prc->c - (w0));
    }
    prc->double_set = ((bool) 0);
    prc->fixed_limit = ((k3 <= sizeof(fixed) * 8 - 3 && (((prc->a) < 0 ? -(prc->a) : (prc->a)) <= (~(-1L << ((1 << 2) * 8 - 1))) >> (k3 + 2)) && (((prc->b) < 0 ? -(prc->b) : (prc->b)) <= (~(-1L << ((1 << 2) * 8 - 1))) >> (k2 + 2)) && (((prc->c) < 0 ? -(prc->c) : (prc->c)) <= (~(-1L << ((1 << 2) * 8 - 1))) >> (k + 1))) ? (1 << k) - 1 : -1);
    prc->cache.ky0 = prc->cache.ky3 = y0;
    prc->cache.xl = x0;
    prc->cache.xd = 0;
}
