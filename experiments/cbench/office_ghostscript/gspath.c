int gs_upathbbox(gs_state * pgs, gs_rect * pbox, bool include_moveto)
{
    gs_fixed_rect fbox;
    gs_rect dbox;
    int code = gx_path_bbox(pgs->path, &fbox);
    if (code < 0)
	return code;
    if ((((pgs->path)->state_flags & ~psf_outside_range) == psf_last_moveto) && include_moveto) {
	gs_fixed_point pt;
	(!(((pgs->path)->state_flags & psf_position_valid) != 0) ? ((-14)) : ((&pt)->x = pgs->path->position.x, (&pt)->y = pgs->path->position.y, 0));
	if (pt.x < fbox.p.x)
	    fbox.p.x = pt.x;
	if (pt.y < fbox.p.y)
	    fbox.p.y = pt.y;
	if (pt.x > fbox.q.x)
	    fbox.q.x = pt.x;
	if (pt.y > fbox.q.y)
	    fbox.q.y = pt.y;
    }
    dbox.p.x = ((fbox.p.x) * (1.0 / (1 << 12)));
    dbox.p.y = ((fbox.p.y) * (1.0 / (1 << 12)));
    dbox.q.x = ((fbox.q.x) * (1.0 / (1 << 12)));
    dbox.q.y = ((fbox.q.y) * (1.0 / (1 << 12)));
    return gs_bbox_transform_inverse(&dbox, &(*(const gs_matrix *) &(pgs)->ctm), pbox);
} int gs_path_enum_init(gs_path_enum * penum, const gs_state * pgs)
{
    gx_path *copied_path = gx_path_alloc(pgs->memory, "gs_path_enum_init");
    int code;
    if (copied_path == 0)
	return ((-25));
    code = gx_path_copy_reducing(pgs->path, copied_path, (~(-1L << ((1 << 2) * 8 - 1))), (1 ? pco_init : pco_none));
    if (code < 0) {
	(*(pgs->memory)->procs.free_object) (pgs->memory, copied_path, "gs_path_enum_init");
	return code;
    }
    gx_path_enum_init(penum, copied_path);
    penum->pgs = pgs;
    penum->copied_path = copied_path;
    return 0;
}

int gs_path_enum_next(gs_path_enum * penum, gs_point ppts[3])
{
    gs_fixed_point fpts[3];
    gs_state *pgs = (gs_state *) penum->pgs;
    int pe_op = gx_path_enum_next(penum, fpts);
    int code;
    switch (pe_op) {
    case 0:
    case 4:
	break;
    case 3:
	if ((code = gs_itransform(pgs, ((fpts[1].x) * (1.0 / (1 << 12))), ((fpts[1].y) * (1.0 / (1 << 12))), &ppts[1])) < 0 || (code = gs_itransform(pgs, ((fpts[2].x) * (1.0 / (1 << 12))), ((fpts[2].y) * (1.0 / (1 << 12))), &ppts[2])) < 0)
	    return code;
    case 1:
    case 2:
	if ((code = gs_itransform(pgs, ((fpts[0].x) * (1.0 / (1 << 12))), ((fpts[0].y) * (1.0 / (1 << 12))), &ppts[0])) < 0)
	    return code;
    default:
	break;
    }
    return pe_op;
}

void gs_path_enum_cleanup(gs_path_enum * penum)
{
    if (penum->copied_path != 0) {
	gx_path_release(penum->copied_path);
	(*(penum->pgs->memory)->procs.free_object) (penum->pgs->memory, penum->copied_path, "gs_path_enum_cleanup");
	penum->path = 0;
	penum->copied_path = 0;
    }
}
