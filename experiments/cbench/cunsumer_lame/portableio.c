int ReadByte(FILE * fp)
{
    int result;
    result = _IO_getc(fp) & 0xff;
    if (result & 0x80)
	result = result - 0x100;
    return result;
}

int Read16BitsLowHigh(FILE * fp)
{
    int first, second, result;
    first = 0xff & _IO_getc(fp);
    second = 0xff & _IO_getc(fp);
    result = (second << 8) + first;
    if (result & 0x8000)
	result = result - 0x10000;
    return (result);
}

int Read16BitsHighLow(FILE * fp)
{
    int first, second, result;
    first = 0xff & _IO_getc(fp);
    second = 0xff & _IO_getc(fp);
    result = (first << 8) + second;
    if (result & 0x8000)
	result = result - 0x10000;
    return (result);
}

